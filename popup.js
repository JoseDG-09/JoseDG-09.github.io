//Funcion para abrir y cerrar el POPUP de la pagina web
var btnAbrirWeb = document.getElementById("abrir-web"),
    btnAbrirWeb2 = document.getElementById("abrir-web2"),
    overlayWeb = document.getElementById("overlay_web"),
    popupWeb = document.getElementById("popup_web"),
    btnCerrarWeb = document.getElementById("btn_cerrar_web");

btnAbrirWeb.addEventListener('click', function () {
    overlayWeb.classList.add('active');
    popupWeb.classList.add('active');
})

btnAbrirWeb2.addEventListener('click', function () {
    overlayWeb.classList.add('active');
    popupWeb.classList.add('active');
})

btnCerrarWeb.addEventListener('click', function () {
    overlayWeb.classList.remove('active');
    popupWeb.classList.remove('active');
})

